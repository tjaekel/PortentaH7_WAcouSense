/* global function to init and start MIC audio */
void PDM_MIC_Init(unsigned long gain, unsigned long freq, int gen_sine)
{
	uint32_t rFreq;
	if (gain)		//stop with gain 0
	{
		/* REMARK: 16000 is just single MIC (left) and mono - why?
		 *
		 * 44100, 22050 etc. not tested and not working!
		 * 8 KHz is OK (stereo)
		 */
		switch (freq)
		{
		case 1 : rFreq = 32000; break;
		case 2 : rFreq = 24000; break;
		case 3 : rFreq = 16000; break;
		case 4 : rFreq =  8000; break;
		default: rFreq = 48000;
		}
		//support negative dB: greater 100 is like -dB,
		//our command line does not support negative values!
		if (gain >= 100)
			sgain = 100 - (int)gain;
		else
			sgain = gain - 1;		//1 is 0 dB!
		if (py_audio_init(2, rFreq, sgain, 0.9883f, gen_sine))
			py_audio_start_streaming();
	}
	else
	{
		extern void USBD_AUDIO_ClearBuffer(void);
		py_audio_stop_streaming();
		py_audio_deinit();
		/* we have to clear the USB buffer, otherwise still last content! */
		////USBD_AUDIO_ClearBuffer();		//this does not help, instead:
		memset(samples, 0, PCM_BUFFER_SIZE);
	}
}

